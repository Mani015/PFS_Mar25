
# Dictionary : python dict is used to store key and value as an pair
# using semi colon ':' seperated key and value


# There are 3 ways to create dictionary

# 1.Create Empty dictionary

d1 = {}
# print(d1)
# print(type(d1))
# {}
# <class 'dict'>


# Using curly braces : Dictionary create by enclosing curly braces , each pair separated by comma

emp = {'Name' : 'chaithnya','Age' : 21, 'Salary' : 1000000}
print(emp)
print(type(emp))
# {'Name': 'chaithnya', 'Age': 21, 'Salary': 1000000}
# <class 'dict'>





# Using with constructor  : dict()

d2 = dict(name = 'santhosh',age = 12,location  = "pune")
print(d2)
print(type(d2))
# {'name': 'santhosh', 'age': 12, 'location': 'pune'}
# <class 'dict'>
