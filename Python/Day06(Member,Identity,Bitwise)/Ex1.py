
# Membership Operator : it returns Boolean value
# There are two types of Operators : 
# 1.in, 2.not in
# Iterables : List,tuple,set,dict,string.........


Names : list[str] = ['Chaithnya','Sonia','Bharath','Darla','Ron','Philip']

# print(Names)

# in : returns True, when the specified value is present in the given object.

# syntax : Value in object

# print('Darla' in Names) True
# print('Ron' in Names)  # True

# in : returns False, when the specified value is not present in the given object.

# print('Andrews' in Names)  # False

# print('Stella' in Names)  # False




# Not in : returns True, when the specified value is not present in the given object.

Num : tuple[int] = (22,55,66,33,88,99)

# print(0 not in Num)  # True

# print(7 not in Num) # True


# not in  : return False ,when the specified value is present in the given object.


# print(22 not in Num) False

print(99 not in Num)  # False






