
# Apply some inbuilt functions on list

l1 = [2,6,10,7,1,9,13,-4,24]

# What's the maximum number in given list

print('maximum value : ', max(l1))
# maximum value :  24

# What's the Minimum number in given list.

print('Minimum value : ', min(l1))
# Minimum value :  -4

# find's the length of list
print('length of list : ', len(l1))
# length of list :  9


# What's the sum of all numbers in given list

print('sum of numbers : ', sum(l1))
# sum of numbers :  68



