
# Transfer statements
# 1.break, 2.continue, 3.pass


# break	Terminate the current loop. Use the break statement to come out of the loop instantly.


Names = ['apple','mango','banana','graphes','guava','pineapple']

for i in Names:

	if i == 'guava':

		break

	print(i)


print('Loop Terminated')
