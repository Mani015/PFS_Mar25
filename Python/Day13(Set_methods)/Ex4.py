
# The isdisjoint() method returns True if none of the items are present in both sets

# sy : set1.isdisjoint(set2)


s1 = {'a','b','c','d'}

s2 = {'e','f','g'}

print(s1.isdisjoint(s2))



# The isdisjoint() method returns False if any item is present in the both sets

s3 = {'p','q','r','s','b'}

print(s1.isdisjoint(s3))

# False



