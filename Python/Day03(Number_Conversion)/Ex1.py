# Binary conversion : Binary prefix should starts with 0b, and it contains only 0's and 1's
# Binary to integer
x = 0b1100
# print(x) 12

y = 0b1010
# print(y)  10


# Integer to binary 
# by using inbuilt method bin() we can convert int to binary 
# sy : bin(integer)

b1 = bin(12)
# print(b1) 0b1100

b2 = bin(14)
# print(b2) 0b1110


