# Octal conversion : prefix should starts with  0o, it contains only number are 0 to 7

# Octal to number 

o1  = 0o123
# print(o1)
# 83

o2 = 0o2564
# print(o2)
# 1396


# Number to octal

# by using inbuilt method oct() we can convert int to octal

t1 = oct(83) 
# print(t1)  0o123

t2 = oct(123)
print(t2)
# 0o173
