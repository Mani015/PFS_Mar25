# float : Float or Floating point data type is used for numeric data with one or more decimals.
# It can be either positive or negative.
a = 12.3
print(a)
print(type(a))
# 12.3
# <class 'float'>


b = float(20)
print(type(b))
print(b)
# <class 'float'>
# 20.0


# complex : The complex number datatype is  one (real  + imaginary)
c = 12 + 4j  # j represent imaginary value
print(type(c))
print(c)
# <class 'complex'>
# (12+4j)


