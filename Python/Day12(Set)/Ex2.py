
# Set ?
# Set is a python inbuilt datatype
# It is used to store multiple value with in a single variable name

# How to we define set
# There are two ways here 
# 1.To create with constructor
# 2.To create with out constructor


s1 = set((1,2,3,4,5,6,7))
print(s1)
# {1, 2, 3, 4, 5, 6, 7}


# Without constructor
s2 = {11,22,33,44}
print(s2)
print(type(s2))
# {33, 11, 44, 22}
# <class 'set'>


